class Person:
    def __init__(self, name):
        self.name = name

    def talk(self):
        print(f"Hi, I am {self.name}")


jhon = Person("Jhon Smith")
jhon.talk()

bob = Person("Bob Smith")
bob.talk()

atharv = Person("Atharv Purohit")
atharv.talk()

kuldeep = Person("Kuldeep Purohit")
kuldeep.talk()



